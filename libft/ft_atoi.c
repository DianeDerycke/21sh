#include "libft.h"

int		ft_atoi(const char *str)
{
	int				i;
	int				nbr;
	unsigned int	neg;

	nbr = 0;
	neg = 0;
	i = 0;
	while (str[i] == '\t' || str[i] == '\n' || str[i] == '\r' ||
			str[i] == '\v' || str[i] == '\f' || str[i] == 32)
		i++;
	if (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			neg = -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		nbr = (nbr * 10) + (str[i] - '0');
		i++;
	}
	return (neg == 0 ? nbr : -nbr);
}
