#include "libft.h"

int		ft_nblen(int nb)
{
	int	cpt;

	cpt = 1;
	if (nb < 0)
		nb = nb * -1;
	if ((nb < 9) && (nb >= 0))
		cpt = 1;
	while (nb > 9)
	{
		nb = nb / 10;
		cpt++;
	}
	return (cpt);
}
